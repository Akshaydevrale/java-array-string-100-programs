package p1;

//Code to print the Square of all element

public class Que15 {

	public static void main(String[] args) {
		int num[]=new int[4];
		num[0]=2;
		num[1]=4;
		num[2]=6;
		num[3]=8;
		
		for(int i=0;i<num.length;i++) {
			System.out.println("Square of:"+num[i]+"="+(num[i]*num[i]));
		}
	}

}
